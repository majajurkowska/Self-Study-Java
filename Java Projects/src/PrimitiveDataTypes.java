public class PrimitiveDataTypes {
    public static void main(String[] args) {

        byte by = 127;
        int i = 200;
        double d = 5.5;
        float f = 6.6f;
        char c = 'A';
        boolean b = false;

        //those are just the primitive types (byte, short, int, long, char, float, double, boolean) for now.
        //there is also "short" and "long" types,
        //those are used for whole numbers with certain ranges,
        //long having a wider range than the int and short a smaller range

        System.out.println(by);
        System.out.println(i);
        System.out.println(d);
        System.out.println(f);
        System.out.println(c);
        System.out.println(b);
    }
}
